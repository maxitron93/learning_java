package java_basics;

public class Variables {

    public static void main(String[] args) {
        // Declaring variables
        int y;
        String words;

        // Assigning variables
        y = 10;
        words = "This is another sentence";

        // Combining declaration and assignment
        int x = 4;
        String sent = "This is a sentence";

        // Printing output
        System.out.println(x + y);
        System.out.println(sent);
        System.out.println(words);
        System.out.println(sent + ". " + words);
    }

}
